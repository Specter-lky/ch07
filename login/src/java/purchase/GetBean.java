public class GetBean {
    private String orderid;
    private String supplier;
    private String phone;
    private String pername;
    private String proname;
    private String amount;
    private String price;
    public GetBean(){
    }
    public String getOrderid() {
        return orderid;
    }
    public void setOrderid(String date) {
        this.orderid = date;
    }
    public String getSupplier() {
        return supplier;
    }
    public void setSupplier(String thing) {
        this.supplier = thing;
    }
    public String getPhone() {
        return phone;
    }
    public void setPhone(String date) {
        this.phone = date;
    }
    public String getPername() {
        return pername;
    }
    public void setPername(String date) {
        this.pername = date;
    }
    public String getProname() {
        return proname;
    }
    public void setProname(String date) {
        this.proname = date;
    }
    public String getAmount() {
        return amount;
    }
    public void setAmount(String date) {
        this.amount = date;
    }
    public String getPrice() {
        return price;
    }
    public void setPrice(String date) {
        this.price = date;
    }
}
